package org.lamport.tla.toolbox.job;

import org.eclipse.core.resources.IResource;
import org.eclipse.core.resources.IWorkspace;
import org.eclipse.core.resources.ResourcesPlugin;
import org.eclipse.core.resources.WorkspaceJob;
import org.eclipse.core.runtime.CoreException;
import org.eclipse.core.runtime.IProgressMonitor;
import org.eclipse.core.runtime.IStatus;
import org.eclipse.core.runtime.Status;
import org.lamport.tla.toolbox.util.ResourceHelper;

/**
 * Delete files out of sync
 */
public class DeleteOutOfSyncJob extends WorkspaceJob {
	private final IResource[] files;

	/**
	 * @param name
	 */
	public DeleteOutOfSyncJob(final IResource[] files) {
		super("deleteOutOfSyncFiles");
		this.files = files;
		this.setRule(ResourceHelper.getDeleteRule(files));
	}

	/**
	 * @see org.eclipse.core.resources.WorkspaceJob#runInWorkspace(org.eclipse.core.runtime.IProgressMonitor)
	 */
	public IStatus runInWorkspace(final IProgressMonitor monitor) throws CoreException {
		final IWorkspace ws = ResourcesPlugin.getWorkspace();
		ws.delete(files, isBlocking(), monitor);
		return Status.OK_STATUS;
	}
}
